def printStrftime(y, m, d)
  print Time.local(y, m, d).strftime("%U, %V, %W\n")
end

def printStrftimeWithYear(y)
  print "[\n"
  printStrftime(y, 1, 1)
  printStrftime(y, 1, 7)
  printStrftime(y, 1, 11)
  printStrftime(y, 12, 21)
  printStrftime(y, 12, 25)
  printStrftime(y, 12, 31)
  print "]\n"
end

